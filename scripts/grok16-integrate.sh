#!/usr/bin/env bash
# Grok16 integrate — auto-wire SG consumers to canonical Grok16 2.0 belt
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
# shellcheck source=grok16-config.sh
source "$SCRIPT_DIR/grok16-config.sh"

QUEEN="$GROK16_QUEEN_ROOT"
SG="$GROK16_SG_ROOT"
ZOCR="$SG/ZOCR"
WR="$SG/World_Redata"

run() { echo "integrate: $1"; "$@"; }

write_consumer_env() {
  local path="$GROK16_ROOT/data/grok16-integrate.env"
  if [[ -f "$GROK16_ROOT/data/g16-always-optimal-panel.json" ]]; then
    echo "integrate: grok16-integrate.env from always-optimal panel"
    return 0
  fi
  cat >"$path" <<EOF
# Auto-generated by grok16-integrate.sh — source for SG consumers
export GROK16_ROOT="${GROK16_ROOT}"
export G16_PREFIX="${G16_PREFIX}"
export GROK16_SG_ROOT="${GROK16_SG_ROOT}"
export GROK16_QUEEN_ROOT="${GROK16_QUEEN_ROOT}"
export G16_BELT_PROFILE="${G16_BELT_PROFILE:-belt_2_0}"
export G16_BENCH_PROFILE="${G16_BENCH_PROFILE:-belt_2_0}"
export G16_ALWAYS_OPTIMAL="1"
export NEXUS_SINGLE_FIELD_DEPTH="${NEXUS_SINGLE_FIELD_DEPTH:-1}"
EOF
  echo "integrate: wrote $path"
}

sync_queen_manifest() {
  [[ -d "$QUEEN/data" ]] || return 0
  run "$GROK16_ROOT/scripts/grok16-toolchain.sh" manifest
  if [[ -f "$GROK16_ROOT/data/grok16-toolchain.json" ]]; then
    cp -f "$GROK16_ROOT/data/grok16-toolchain.json" "$QUEEN/data/g16-toolchain.json"
    echo "integrate: Queen/data/g16-toolchain.json ← canonical"
  fi
  local delegate="$QUEEN/scripts/g16-toolchain.sh"
  if [[ -f "$delegate" ]]; then
    grep -q 'Grok16/scripts/grok16-toolchain.sh' "$delegate" || \
      echo "integrate: warn — Queen delegate may not point at Grok16" >&2
  fi
}

sync_zocr_field_compiler() {
  [[ -d "$ZOCR/data" ]] || return 0
  local fc="$ZOCR/data/field-compiler.json"
  g16_gpy_run - <<PY
import json
from pathlib import Path
fc = Path("${fc}")
doc = {}
if fc.is_file():
    try:
        doc = json.loads(fc.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        pass
doc.setdefault("schema", "zocr-field-compiler/v1")
doc["grok16"] = {
    "root": "${GROK16_ROOT}",
    "prefix": "${G16_PREFIX}",
    "distro": "2.0.0",
    "belt_profile": "${G16_BELT_PROFILE:-belt_2_0}",
    "toolchain": "scripts/grok16-toolchain.sh",
    "integrate_env": "data/grok16-integrate.env",
}
doc["canonical"] = "SG/Grok16"
fc.parent.mkdir(parents=True, exist_ok=True)
fc.write_text(json.dumps(doc, indent=2) + "\n", encoding="utf-8")
print("integrate: ZOCR field-compiler.json updated")
PY
}

sync_world_redata() {
  [[ -d "$WR" ]] || return 0
  cat >"$WR/.grok16-env" <<EOF
GROK16_ROOT=${GROK16_ROOT}
G16_PREFIX=${G16_PREFIX}
WRDT_G16_PREFIX=${G16_PREFIX}
G16_BELT_PROFILE=${G16_BELT_PROFILE:-belt_2_0}
EOF
  echo "integrate: World_Redata/.grok16-env"
}

dedupe_gpy_path() {
  # Prefer in-tree GPY-16; GrokPy is legacy fallback only
  if [[ -x "$G16_PREFIX/bin/gpy-16" ]]; then
    export GPY16_DRIVER="$G16_PREFIX/bin/gpy-16"
    export GPY16_ROOT="$GROK16_ROOT/python"
    echo "integrate: GPY-16 canonical → $GPY16_DRIVER"
  fi
}

sync_always_optimal() {
  local ao_py="$GROK16_ROOT/lib/field-always-optimal.py"
  [[ -f "$ao_py" ]] || return 0
  if GROK16_SG_ROOT="${GROK16_SG_ROOT:-$GROK16_SG_ROOT}" NEXUS_STATE_DIR="${NEXUS_STATE_DIR:-}" \
     python3 "$ao_py" apply --no-layers 2>/dev/null; then
    echo "integrate: always optimal — belt + runner + power sort synced"
  else
    python3 "$ao_py" apply 2>&1 | head -5 >&2 || true
    echo "integrate: warn — always-optimal partial" >&2
  fi
  # shellcheck source=/dev/null
  [[ -f "$GROK16_ROOT/data/grok16-integrate.env" ]] && source "$GROK16_ROOT/data/grok16-integrate.env"
}

sync_power_sort() {
  local ps_py="$GROK16_ROOT/lib/field-power-sort.py"
  [[ -f "$ps_py" ]] || return 0
  if GROK16_SG_ROOT="${GROK16_SG_ROOT:-$GROK16_SG_ROOT}" NEXUS_STATE_DIR="${NEXUS_STATE_DIR:-}" \
     python3 "$ps_py" apply --no-bench 2>/dev/null; then
    echo "integrate: power sort — best algorithms pinned"
  else
    python3 "$ps_py" apply 2>&1 | head -3 >&2 || true
    echo "integrate: warn — power-sort partial" >&2
  fi
}

sync_nexus_g16_integrate() {
  local nexus="${GROK16_SG_ROOT:-$SG}/NewLatest"
  local rc_py="$nexus/lib/nexus-g16-recompile.py"
  [[ -f "$rc_py" ]] || return 0
  g16_gpy_run - <<PY
import json
from pathlib import Path
nexus = Path("${nexus}")
doc_path = nexus / "data" / "nexus-g16-integrate-doctrine.json"
doc = {}
if doc_path.is_file():
    try:
        doc = json.loads(doc_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        pass
doc.setdefault("schema", "nexus-g16-integrate-doctrine/v1")
doc["grok16_integrated"] = True
doc["grok16_root"] = "${GROK16_ROOT}"
doc["combinatronics"] = {
    **(doc.get("combinatronics") or {}),
    "gate": "Grok16/lib/g16-compile-combinatronics.py",
    "always_at_creation": True,
}
doc_path.write_text(json.dumps(doc, indent=2) + "\\n", encoding="utf-8")
print("integrate: nexus-g16-integrate-doctrine.json stamped")
PY
  echo "integrate: NewLatest nexus-g16-recompile wired"
}

sync_compile_combinatronics_env() {
  local env_path="$GROK16_ROOT/data/grok16-integrate.env"
  [[ -f "$env_path" ]] || return 0
  grep -q 'G16_OPTIMAL_COMBINATRONICS_AT_COMPILE' "$env_path" 2>/dev/null || \
    echo 'export G16_OPTIMAL_COMBINATRONICS_AT_COMPILE=1' >>"$env_path"
  grep -q 'NEXUS_G16_INTEGRATED' "$env_path" 2>/dev/null || \
    echo 'export NEXUS_G16_INTEGRATED=1' >>"$env_path"
  echo "integrate: combinatronics-at-compile env pinned"
}

sync_field_research_book() {
  local fr_py="$GROK16_ROOT/lib/field-research-book.py"
  [[ -f "$fr_py" ]] || return 0
  if python3 "$fr_py" publish >/dev/null 2>&1; then
    echo "integrate: Field Research book panel published"
  else
    python3 "$fr_py" publish 2>&1 | head -3 >&2 || true
    echo "integrate: warn — field-research-book publish incomplete" >&2
  fi
}

cmd_integrate() {
  echo "integrate: Grok16 2.0 belt — SG auto-wire"
  dedupe_gpy_path
  write_consumer_env
  sync_queen_manifest
  sync_zocr_field_compiler
  sync_world_redata
  sync_always_optimal
  sync_nexus_g16_integrate
  sync_compile_combinatronics_env
  sync_field_research_book
  if [[ -x "$GROK16_ROOT/scripts/grok16-bench-triad.sh" ]] && \
     "$GROK16_ROOT/scripts/grok16-toolchain.sh" status >/dev/null 2>&1; then
    run "$GROK16_ROOT/scripts/grok16-bench-triad.sh" triad || \
      echo "integrate: triad skipped (compiler not ready)" >&2
  else
    echo "integrate: skip triad (g16 not ready)"
  fi
  echo "integrate: PASS — consumers wired, compat with 1.0 belt_1_0 profile"
}

case "${1:-integrate}" in
  integrate|"") cmd_integrate ;;
  *)
    echo "Usage: $0 [integrate]" >&2
    exit 2
    ;;
esac